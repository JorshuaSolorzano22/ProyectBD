private void loadProductsToComboBoxes() {
    // Cargar los productos en los ComboBox
    for (Product product : products) {
        String categoryName = getCategoryName(product.getCodigoTipoProducto());
        
        // Agregar los productos al ComboBox correspondiente seg√∫n el nombre de la categor√≠a
        if (categoryName.equals("Hamburguesas")) {
            cmbHamburguesas.addItem(product.getNombreProducto());
        } else if (categoryName.equals("Refrescos")) {
            cmbRefrescos.addItem(product.getNombreProducto());
        } else if (categoryName.equals("Combos")) {
            cmbCombos.addItem(product.getNombreProducto());
        }
    }
}

private String getCategoryName(int codigoTipoProducto) {
   
    switch (codigoTipoProducto) {
        case 1:
            return "Hamburguesas";
        case 2:
            return "Refrescos";
        case 3:
            return "Combos";
        default:
            return "Desconocido";
    }
}

    public void saveOrder(JTextArea txtPedido) {
        StringBuilder textBuilder = new StringBuilder("Pedido: \n");

        // Obtener el producto de hamburguesa seleccionado y la cantidad
        String selectedHamburguer = (String) cmbHamburguesas.getSelectedItem();
        int quantityHamburguer = (int) spnHamburguesas.getValue();
        if (!selectedHamburguer.equals("Elegir...") && quantityHamburguer > 0) {
            textBuilder.append(quantityHamburguer).append(" ").append(selectedHamburguer).append("\n");
        }

        // Obtener el producto de refresco seleccionado y la cantidad
        String selectedRefresco = (String) cmbRefrescos.getSelectedItem();
        int quantityRefresco = (int) spnRefrescos.getValue();
        if (!selectedRefresco.equals("Elegir...") && quantityRefresco > 0) {
            textBuilder.append(quantityRefresco).append(" ").append(selectedRefresco).append("\n");
        }

        // Obtener el producto de combo seleccionado y la cantidad
        String selectedCombo = (String) cmbCombos.getSelectedItem();
        int quantityCombo = (int) spnCombos.getValue();
        if (!selectedCombo.equals("Elegir...") && quantityCombo > 0) {
            textBuilder.append(quantityCombo).append(" ").append(selectedCombo).append("\n");
        }

        // Agregar la informaci√≥n al txtPedido
        txtPedido.setText(textBuilder.toString());
    }


    public static void llenarArray() throws SQLException {
        try {
            // Limpiar la lista de productos
            productos.clear();

            // Cargar los productos desde la fuente de datos
            metodos.cargarObjProduc(productos);

            // Obtener el ID del producto a eliminar
            int ID = (Integer) txtSearch.getValue();

            // Buscar el producto en la lista por ID
            Product productoAEliminar = null;
            for (Product producto : productos) {
                if (producto.getCodigoProducto() == ID) {
                    productoAEliminar = producto;
                    break;
                }
            }

            // Mostrar un mensaje de confirmación
            if (productoAEliminar != null) {
                String mensaje = "¿Estás seguro de que quieres eliminar el siguiente producto?\n"
                        + "ID: " + productoAEliminar.getCodigoProducto() + "\n"
                        + "Nombre: " + productoAEliminar.getNombreProducto();
                int respuesta = JOptionPane.showConfirmDialog(null, mensaje, "Confirmar Eliminación", JOptionPane.YES_NO_OPTION);

                // Si el usuario confirma la eliminación, llamar al método para eliminar el producto
                if (respuesta == JOptionPane.YES_OPTION) {
                    metodos.deleteProduct(ID);
                    JOptionPane.showMessageDialog(null, "Eliminacion concretada.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró ningún producto con el ID especificado.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(EliminarProducto.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Error al intentar eliminar el producto.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

